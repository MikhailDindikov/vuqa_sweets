class CandyModel {
  final String fotka;
  final String nazv;
  final List<int> tsennik;
  bool wasBought;

  CandyModel(this.fotka, this.nazv, this.tsennik, this.wasBought);
  factory CandyModel.f(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.fff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.fffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.fffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ffffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.fffffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ffffffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.fffffffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
  factory CandyModel.ffffffffffff(fotka, nazv, tsennik, wasBought) =>
      CandyModel(fotka, nazv, tsennik, wasBought);
}
